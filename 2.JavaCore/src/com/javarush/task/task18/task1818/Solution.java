package com.javarush.task.task18.task1818;

/* 
Два в одном
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        String fileName1 = reader.readLine();
        String fileName2 = reader.readLine();
        String fileName3 = reader.readLine();
        FileOutputStream out = new FileOutputStream(fileName1);
        FileInputStream in2 = new FileInputStream(fileName2);
        FileInputStream in3 = new FileInputStream(fileName3);

        while (in2.available() > 0){
            out.write(in2.read());
        }
        out.close();
        in2.close();
        out = new FileOutputStream(fileName1, true);
        while (in3.available() > 0){
            out.write(in3.read());
        }
        in3.close();
    }
}
/*Два в одном
Считать с консоли 3 имени файла.
Записать в первый файл содержимого второго файла, а потом дописать в первый файл содержимое третьего файла.
Закрыть потоки.


Требования:
1. Программа должна три раза считать имена файлов с консоли.
2. Для первого файла создай поток для записи. Для двух других - потоки для чтения.
3. Содержимое второго файла нужно переписать в первый файл.
4. Содержимое третьего файла нужно дописать в первый файл (в который уже записан второй файл).
5. Созданные для файлов потоки должны быть закрыты.*/